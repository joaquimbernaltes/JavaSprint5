    /* Establim els paràmetres del Frame */
    public JFrameModificarPropostes(LlistaEntitats llista_enti, LlistaPropostes llista_prop, LlistaProjectes llista_proj,
                                    LlistaPresupostos llista_pres , int selectedItemEntitat, String nom, String descripcio, String data_alta) {
        try {
           //
            ll_enti = llista_enti;
            ll_proj = llista_proj;
            ll_prop = llista_prop;
            JFrameModificarPropostes.selectedItemEntitat = selectedItemEntitat;
            JFrameModificarPropostes.nom = nom;
            JFrameModificarPropostes.descripcio = descripcio;
            JFrameModificarPropostes.data_alta = data_alta;

            initComponents();

            prop = ll_prop.returnList().get(selectedItem);
            if (selectedItemEntitat != -1) {
                //Agafar el valor de l'entitat d'una taula
                Entitat e = ll_enti.returnList().get(selectedItemEntitat);
                field_entitat.setText(e.getNom());
                en = e.getNom();
            }
            field_nom.setText(prop.getNom());
            field_descripcio.setText(prop.getDescripcio());
            field_dataAlta.setText(prop.getDataAlta());
            field_entitat.setText(en);
            if ("Activa".equals(prop.getEstat())) {
                botoAlta.setVisible(false);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }


