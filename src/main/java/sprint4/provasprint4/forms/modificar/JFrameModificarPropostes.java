    private void modificarPropostaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_modificarPropostaMouseClicked
        //Mostrem missatge de confirmació
        int confirm = JOptionPane.showConfirmDialog(this, "Esteu segurs de modificar aquesta propsota?");
        if (confirm == JOptionPane.YES_OPTION) {
            if (prop.getEstat().equals("Activa")) {
                //Cridem al metode per a modificar les dades
                Entitat enti = ll_enti.buscarPerNom(field_entitat.getText(), selectedItemEntitat);
                ll_prop.modificarProposta(selectedItem, field_nom.getText(), field_descripcio.getText(), enti, field_dataAlta.getText(), "", "Activa");
            } else {
                //Cridem al metode per a modificar les dades
                Entitat enti = ll_enti.buscarPerNom(field_entitat.getText(), selectedItemEntitat);
                ll_prop.modificarProposta(selectedItem, field_nom.getText(), field_descripcio.getText(), enti, field_dataAlta.getText(), prop.getDataBaixa(), "Inactiva");
            }
            enrereMouseClicked(evt);
        }
    }//GEN-LAST:event_modificarPropostaMouseClicked

