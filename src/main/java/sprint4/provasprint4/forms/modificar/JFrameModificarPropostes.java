    private void botoAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botoAltaActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Esteu segurs de modificar aquesta proposta?");
        if (confirm == JOptionPane.YES_OPTION) {
            //Metode que retorna una entitat, passantli el seu nom i id.
            Entitat enti = ll_enti.buscarPerNom(field_entitat.getText(), selectedItemEntitat);
            System.out.println(prop.getEstat());
            //Donar d'alta una matricula que esta de baixa
            ll_prop.modificarProposta(selectedItem, field_nom.getText(), field_descripcio.getText(), enti, field_dataAlta.getText(), "", "Activa");

            // Tornem a la finestra principal
            JFramePropostes principalWindow = new JFramePropostes(ll_enti, ll_proj, ll_prop,ll_pres);
            principalWindow.setVisible(true);       //la fem visible
            dispose();                             //eliminem la finestra actual      
        }
    }//GEN-LAST:event_botoAltaActionPerformed

