    /* Establim els paràmetres del Frame */
    public JFrameModificarPressuposts(LlistaEntitats llista_enti, LlistaPropostes llista_prop, LlistaProjectes llista_proj,
                                      LlistaPresupostos llista_pres , int selectedItemEntitat, String nom, String descripcio, String data_alta) {
        try {
           //
            ll_enti = llista_enti;
            ll_proj = llista_proj;
            ll_prop = llista_prop;
            ll_pres = llista_pres;
            JFrameModificarPressuposts.selectedItemEntitat = selectedItemEntitat;
            JFrameModificarPressuposts.nom = nom;
            JFrameModificarPressuposts.descripcio = descripcio;
            JFrameModificarPressuposts.data_alta = data_alta;

            initComponents();

            pres = ll_pres.returnList().get(selectedItem);
            if (selectedItemEntitat != -1) {
                //Agafar el valor de l'entitat d'una taula
                Entitat e = ll_enti.returnList().get(selectedItemEntitat);
                field_entitat.setText(e.getNom());
                en = e.getNom();
            }
            field_nom.setText(pres.getNom());
            field_descripcio.setText(pres.getDescripcio());
            field_dataAlta.setText(pres.getDataAlta());
            field_entitat.setText(en);
            if ("Activa".equals(pres.getEstat())) {
                botoAlta.setVisible(false);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }


