    private void modificarPressupostMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_modificarPressupostMouseClicked
        //Mostrem missatge de confirmació
        int confirm = JOptionPane.showConfirmDialog(this, "Esteu segurs de modificar aquest pressupost?");
        if (confirm == JOptionPane.YES_OPTION) {
            if (pres.getEstat().equals("Activa")) {
                //Cridem al metode per a modificar les dades
                Projecte proj = ll_proj.buscarPerNom(field_entitat.getText(), selectedItemEntitat);
                ll_pres.modificarPressupost(selectedItem, field_nom.getText(), field_descripcio.getText(), proj, field_dataAlta.getText(), "", "Activa");
            } else {
                //Cridem al metode per a modificar les dades
                Projecte projecte = ll_proj.buscarPerNom(field_entitat.getText(), selectedItemEntitat);
                ll_pres.modificarPressupost(selectedItem, field_nom.getText(), field_descripcio.getText(), projecte, field_dataAlta.getText(), pres.getDataBaixa(), "Inactiva");
            }
            enrereMouseClicked(evt);
        }
    }//GEN-LAST:event_modificarPressupostMouseClicked

